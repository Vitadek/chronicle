#!/usr/bin/env node
/**
 * Regenerates src/lib/autocomplete/wordlist.ts from Peter Norvig's unigram
 * counts (Google Web Trillion Word Corpus) — the standard frequency source
 * for non-AI completion and spelling work.
 *
 *   node scripts/build-wordlist.mjs [path-to-count_1w.txt]
 *
 * Without an argument it downloads https://norvig.com/ngrams/count_1w.txt
 * (~5 MB). Curation for prose autocomplete:
 *
 *  - lowercase ascii-alpha words only (the corpus is already lowercased)
 *  - length ≥ 4: with a 2-char minimum prefix and a 2-char minimum ghost
 *    suffix, nothing shorter can ever be suggested, so shipping it is waste
 *  - a tiny denylist of web-corpus artifacts that aren't prose
 *  - top 25,000 by count, emitted most-frequent-first (index = rank)
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE_URL = 'https://norvig.com/ngrams/count_1w.txt';
const KEEP = 25_000;
const WEB_ARTIFACTS = new Set([
  'http', 'https', 'html', 'aspx', 'php', 'href', 'jpeg', 'gif', 'src',
  'www', 'webpage', 'homepage', 'username', 'login', 'logout', 'email',
]);

const raw = process.argv[2]
  ? fs.readFileSync(process.argv[2], 'utf8')
  : await (await fetch(SOURCE_URL)).text();

const words = [];
for (const line of raw.split('\n')) {
  const word = line.split('\t')[0];
  if (!/^[a-z]{4,}$/.test(word) || WEB_ARTIFACTS.has(word)) continue;
  words.push(word);
  if (words.length === KEEP) break;
}
if (words.length < KEEP) throw new Error(`only ${words.length} words survived curation`);

const out = `// GENERATED by scripts/build-wordlist.mjs — do not edit by hand.
// ${KEEP} most frequent English words (length ≥ 4) from Norvig's count_1w.txt
// (Google Web Trillion Word Corpus), most frequent first: index = rank.
// Loaded lazily (dynamic import) so it never weighs down the initial bundle.
export const WORDLIST = \`${words.join('\n')}\`;
`;

const dest = path.join(
  path.dirname(fileURLToPath(import.meta.url)),
  '..', 'src', 'lib', 'autocomplete', 'wordlist.ts',
);
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, out);
console.log(`wrote ${dest}: ${words.length} words, ${(out.length / 1024).toFixed(0)} KiB`);
